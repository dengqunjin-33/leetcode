class Solution {
   public List<Integer> inorderTraversal(LeetCodeTree.TreeNode root) {
       List<Integer> res = new ArrayList<>();
       Deque<LeetCodeTree.TreeNode> stk = new LinkedList<>();
       while (root != null || !stk.isEmpty()) {
           while (root != null) {
               stk.push(root);
               root = root.left;
           }
           root = stk.pop();
           res.add(root.val);
           root = root.right;
       }
       return res;
    }
}
